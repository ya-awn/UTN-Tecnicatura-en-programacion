public class AnalisisCodigo {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // declara e inicializa una variable
        // expresion 10 (genera un valor)
        int x = 10;
        
        // agrega un valor a la variable x
        // "x + 5" (suma que produce un nuevo valor)
        x = x + 5;
        
        // llamada del metodo para imprimir en consola
        // "x" (valor de la variable que se pasa como argumento)
        System.out.println(x);

        //Diferencia:
        // Una expresión es cualquier fragmento de código que produce
        // un valor (como 10 o x + 5). Una instrucción es una orden completa
        // que realiza una acción, como declarar variables, asignar valores o
        // ejecutar métodos; puede contener una o varias expresiones dentro de ella.
    }
    
}
